@register_command("generate_device_proxy")
def generate_device_proxy(params: dict) -> dict:
    """Generate a Python proxy class for a placed device.

    Inspects the device actor, reads its class hierarchy and properties,
    detects events/delegates, and writes a proxy .py file to
    Saved/WellVersed/proxies/.

    Parameters
    ----------
    params.actor_name : str
        Name or label of the device actor in the level.
    """
    if not unreal:
        return {"status": "error", "error": "unreal module not available"}

    actor_name = params["actor_name"]
    actor = _find_actor_by_name(actor_name)
    if actor is None:
        return {"status": "error", "error": f"Actor not found: {actor_name}"}

    class_name = actor.get_class().get_name()
    class_path = actor.get_class().get_path_name()
    hierarchy = _get_class_hierarchy(actor)
    properties = _get_property_info(actor)
    events = _detect_events(properties)

    proxy_code = _generate_proxy_code(
        class_name, class_path, hierarchy, properties, events
    )

    # Write proxy file
    safe_name = re.sub(r"[^a-zA-Z0-9_]", "_", class_name)
    file_path = _proxies_dir() / f"{safe_name}_proxy.py"
    with open(file_path, "w", encoding="utf-8") as f:
        f.write(proxy_code)

    # Also write a JSON metadata file
    meta_path = _proxies_dir() / f"{safe_name}_meta.json"
    meta = {
        "class_name": class_name,
        "class_path": class_path,
        "hierarchy": hierarchy,
        "property_count": len(properties),
        "event_count": len(events),
        "events": events,
        "properties": properties,
        "generated_at": time.time(),
    }
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2)

    return {
        "status": "ok",
        "data": {
            "class_name": class_name,
            "proxy_file": str(file_path),
            "meta_file": str(meta_path),
            "property_count": len(properties),
            "event_count": len(events),
            "events": events,
        },
    }


@register_command("generate_all_proxies")
def generate_all_proxies(params: dict) -> dict:
    """Generate proxy classes for every device in the level.

    Scans all actors, identifies those that look like Creative devices
    (based on class name heuristics), and generates a proxy for each
    unique device class.

    Parameters
    ----------
    params.class_filter : str, optional
        Only generate proxies for classes containing this substring.
    """
    if not unreal:
        return {"status": "error", "error": "unreal module not available"}

    class_filter = params.get("class_filter", "").lower()
    all_actors = unreal.EditorLevelLibrary.get_all_level_actors()

    # Deduplicate by class path
    seen_classes: dict[str, object] = {}
    for actor in all_actors:
        class_name = actor.get_class().get_name()
        class_path = actor.get_class().get_path_name()

        # Heuristic: is this a device?
        name_lower = class_name.lower()
        is_device = any(kw in name_lower for kw in [
            "device", "creative", "manager", "spawner", "trigger",
            "controller", "tracker", "zone", "barrier", "teleporter",
            "vending", "timer", "scoreboard", "button", "switch",
        ])

        if not is_device:
            continue

        if class_filter and class_filter not in name_lower:
            continue

        if class_path not in seen_classes:
            seen_classes[class_path] = actor

    # Generate proxy for each unique class
    generated = []
    errors = []

    for class_path, actor in seen_classes.items():
        try:
            result = generate_device_proxy({"actor_name": actor.get_name()})
            if result.get("status") == "ok":
                generated.append(result["data"])
            else:
                errors.append({
                    "class": actor.get_class().get_name(),
                    "error": result.get("error", "Unknown error"),
                })
        except Exception as exc:
            errors.append({
                "class": actor.get_class().get_name(),
                "error": str(exc),
            })

    # Write an index file
    index_path = _proxies_dir() / "_index.json"
    index_data = {
        "generated_at": time.time(),
        "proxy_count": len(generated),
        "proxies": [
            {"class": g["class_name"], "file": g["proxy_file"]}
            for g in generated
        ],
    }
    with open(index_path, "w", encoding="utf-8") as f:
        json.dump(index_data, f, indent=2)

    return {
        "status": "ok",
        "data": {
            "generated": len(generated),
            "errors": len(errors),
            "proxies": generated,
            "error_details": errors[:20],
            "index_file": str(index_path),
        },
    }
