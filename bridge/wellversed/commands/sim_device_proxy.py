"""Device proxy generation -- generate Python proxy classes from placed devices.

Inspects devices in the level, reads their class hierarchy and properties,
queries digest schema if available, and generates Python proxy classes with
property accessors, method stubs, and event names. Proxies are written to
Saved/WellVersed/proxies/ so Claude can understand device APIs without docs.
"""

from __future__ import annotations

import json
import os
import re
import time
from pathlib import Path

from ..listener import register_command
